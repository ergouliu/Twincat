///////////////////////////////////////////////////////////////////////////////
BOOL CTcAsyncBufferWritingModule::FillBuffer
(
	float* pBuffer, 
	UINT nBuffer, 
	UINT& nBufferIndex
)
{
	m_counter++;

	if (nBufferIndex<nBuffer)
	{
		pBuffer[nBufferIndex] = static_cast<float>(m_counter);
		++nBufferIndex;
		if (nBufferIndex == nBuffer)
		{
			nBufferIndex = 0;
		}
		return TRUE;
	}
	else
	{
		return FALSE;
	}
}


///////////////////////////////////////////////////////////////////////////////
HRESULT CTcAsyncBufferWritingModule::AddModuleToCaller()
{
	HRESULT hr = S_OK;
	if ( m_spCyclicCaller.HasOID() )
	{
		if ( SUCCEEDED_DBG(hr = m_spSrv->TcQuerySmartObjectInterface(m_spCyclicCaller)) )
		{
			if ( FAILED(hr = m_spCyclicCaller->AddModule(m_spCyclicCaller, THIS_CAST(ITcCyclic))) )
			{
				m_spCyclicCaller = NULL;
			}
		}
	}
	else
	{
		hr = ADS_E_INVALIDOBJID; 
		SUCCEEDED_DBGT(hr, "Invalid OID specified for caller task");
	}
	return hr;
}

///////////////////////////////////////////////////////////////////////////////
VOID CTcAsyncBufferWritingModule::RemoveModuleFromCaller()
{
	if ( m_spCyclicCaller )
	{
		m_spCyclicCaller->RemoveModule(m_spCyclicCaller);
	}
	m_spCyclicCaller	= NULL;
}

