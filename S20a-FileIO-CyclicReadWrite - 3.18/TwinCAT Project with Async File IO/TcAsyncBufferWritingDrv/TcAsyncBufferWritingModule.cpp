///<AutoGeneratedContent id="ImplementationOf_ITcCyclic">
HRESULT CTcAsyncBufferWritingModule::CycleUpdate(ITcTask* ipTask, ITcUnknown* ipCaller, ULONG_PTR context)
{
	HRESULT hr = S_OK;

	if (FillBuffer(m_pBufferFill, ASYNCWRITE_BUFFERSIZE, m_nBufferFillIndex))
	{
		if (m_nBufferFillIndex == 0)
		{
			// this indicates that the buffer is full, switch to other buffer
			if (m_pBufferFill == m_Buffer1)
			{
				m_pBufferFill = m_Buffer2;
				m_pBufferWrite = m_Buffer1; 
			}
			else
			{
				m_pBufferFill = m_Buffer1;
				m_pBufferWrite = m_Buffer2;
			}
		}
	}

	if (!m_Inputs.bDisableWriting)
	{
		if (m_pBufferWrite)
		{
			hr = 
				m_fsmFileWriter.Init
				(
					m_spSrv,
					m_spFileAccessAsync,
					"%TC_BOOTPRJPATH%AsyncTest.txt",
					reinterpret_cast<PBYTE>(m_pBufferWrite), 
					ASYNCWRITE_BUFFERSIZE*sizeof(float)
				); 
		}
		if (SUCCEEDED(hr))
		{
			m_pBufferWrite = NULL;
		}
	}

	if (m_fsmFileWriter.IsActive())
	{
		m_Outputs.bBusy = TRUE;
		hr = m_fsmFileWriter.Eval();
		if (FAILED_AND_NOT_PENDING(hr))
		{
			m_Outputs.bError = TRUE;
			m_Outputs.iErrorId = hr;
		}
		else if (SUCCEEDED(hr))
		{
			m_Outputs.nBytesWritten = m_fsmFileWriter.GetBytesWrittenTotal();
			m_Trace.Log(tlVerbose, FLEAVEA "Written bytes: nCopySize=0x%08x", m_Outputs.nBytesWritten);
		}
	}
	else
	{
		m_Outputs.bBusy = FALSE;
	}
	return hr;
}
